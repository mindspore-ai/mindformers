"""Data generation utilities for TransformerLayer test"""
import numpy as np
import mindspore as ms

# Default configuration for data generation
# These can be overridden by arguments in the test script if needed
DEFAULT_SEQ_LENGTH = 2
DEFAULT_BATCH_SIZE = 4
DEFAULT_HIDDEN_SIZE = 24
DEFAULT_FFN_HIDDEN_SIZE = 48  # Typically 2 * hidden_size, matching test_transformer_layer
DEFAULT_NUM_HEADS = 4
DEFAULT_POST_LAYER_NORM = True


def get_init_params(seq_length=DEFAULT_SEQ_LENGTH,
                    batch_size=DEFAULT_BATCH_SIZE,
                    hidden_size=DEFAULT_HIDDEN_SIZE,
                    compute_dtype=ms.bfloat16):
    """
    Generates initial parameters (inputs) for the TransformerLayer model.
    """
    np.random.seed(42)
    hidden_states_shape = (seq_length, batch_size, hidden_size)
    hidden_states_np = 0.01 * np.random.randn(*hidden_states_shape).astype(np.float32) # Initial data in fp32
    attention_mask_np = np.random.choice([True, False], size=(batch_size, 1, seq_length, seq_length)).astype(np.int32)
    init_params = {
        "hidden_states": ms.Tensor(hidden_states_np, dtype=compute_dtype),
        "attention_mask": ms.Tensor(attention_mask_np, dtype=compute_dtype), # Usually bool or float
    }
    return init_params


def get_fixed_weights(hidden_size=DEFAULT_HIDDEN_SIZE,
                      ffn_hidden_size=DEFAULT_FFN_HIDDEN_SIZE,
                      num_layers=1,
                      post_layer_norm=True,
                      param_init_dtype=ms.float32):
    """Generate fixed weights for TransformerBlock.

    Args:
        hidden_size (int, optional): Hidden dimension of the transformer layer. Default: DEFAULT_HIDDEN_SIZE.
        ffn_hidden_size (int, optional): Feed-forward network hidden dimension. Default: DEFAULT_FFN_HIDDEN_SIZE.
        num_layers (int, optional): Number of transformer layers. Default: 1.
        post_layer_norm (bool, optional): Whether to use post layer norm. Default: True.
        param_init_dtype (mindspore.dtype, optional): Data type for parameter initialization. Default: ms.float32.

    Returns:
        dict: Dictionary containing initialized transformer block weights.
    """
    np.random.seed(42)
    weight_dict = {}

    # Generate weights for each layer
    for layer_id in range(num_layers):
        layer_prefix = f"layers.{layer_id}."

        # QKV weights and bias
        qkv_weight = 0.01 * np.random.randn(3 * hidden_size, hidden_size).astype(np.float32)
        qkv_bias = 0.01 * np.random.randn(3 * hidden_size).astype(np.float32)
        weight_dict[f"{layer_prefix}self_attention.linear_qkv.weight"] = ms.Tensor(qkv_weight, dtype=param_init_dtype)
        weight_dict[f"{layer_prefix}self_attention.linear_qkv.bias"] = ms.Tensor(qkv_bias, dtype=param_init_dtype)

        # Projection weights and bias
        proj_weight = 0.01 * np.random.randn(hidden_size, hidden_size).astype(np.float32)
        proj_bias = 0.01 * np.random.randn(hidden_size).astype(np.float32)
        weight_dict[f"{layer_prefix}self_attention.linear_proj.weight"] = ms.Tensor(proj_weight, dtype=param_init_dtype)
        weight_dict[f"{layer_prefix}self_attention.linear_proj.bias"] = ms.Tensor(proj_bias, dtype=param_init_dtype)

        # Input layer norm
        input_ln_gamma = 0.01 * np.random.randn(hidden_size).astype(np.float32)
        input_ln_beta = 0.01 * np.random.randn(hidden_size).astype(np.float32)
        weight_dict[f"{layer_prefix}input_layernorm.gamma"] = ms.Tensor(input_ln_gamma, dtype=param_init_dtype)
        weight_dict[f"{layer_prefix}input_layernorm.beta"] = ms.Tensor(input_ln_beta, dtype=param_init_dtype)

        # Pre-MLP layer norm
        pre_mlp_ln_gamma = 0.01 * np.random.randn(hidden_size).astype(np.float32)
        pre_mlp_ln_beta = 0.01 * np.random.randn(hidden_size).astype(np.float32)
        weight_dict[f"{layer_prefix}pre_mlp_layernorm.gamma"] = ms.Tensor(pre_mlp_ln_gamma, dtype=param_init_dtype)
        weight_dict[f"{layer_prefix}pre_mlp_layernorm.beta"] = ms.Tensor(pre_mlp_ln_beta, dtype=param_init_dtype)

        # MLP weights and bias
        mlp_fc1_weight = 0.01 * np.random.randn(ffn_hidden_size, hidden_size).astype(np.float32)
        mlp_fc1_bias = 0.01 * np.random.randn(ffn_hidden_size).astype(np.float32)
        weight_dict[f"{layer_prefix}mlp.linear_fc1.weight"] = ms.Tensor(mlp_fc1_weight, dtype=param_init_dtype)
        weight_dict[f"{layer_prefix}mlp.linear_fc1.bias"] = ms.Tensor(mlp_fc1_bias, dtype=param_init_dtype)

        mlp_fc2_weight = 0.01 * np.random.randn(hidden_size, ffn_hidden_size).astype(np.float32)
        mlp_fc2_bias = 0.01 * np.random.randn(hidden_size).astype(np.float32)
        weight_dict[f"{layer_prefix}mlp.linear_fc2.weight"] = ms.Tensor(mlp_fc2_weight, dtype=param_init_dtype)
        weight_dict[f"{layer_prefix}mlp.linear_fc2.bias"] = ms.Tensor(mlp_fc2_bias, dtype=param_init_dtype)

    # Final layer norm (if post_layer_norm is True)
    if post_layer_norm:
        final_ln_gamma = 0.01 * np.random.randn(hidden_size).astype(np.float32)
        final_ln_beta = 0.01 * np.random.randn(hidden_size).astype(np.float32)
        weight_dict["final_layernorm.gamma"] = ms.Tensor(final_ln_gamma, dtype=param_init_dtype)
        weight_dict["final_layernorm.beta"] = ms.Tensor(final_ln_beta, dtype=param_init_dtype)

    return weight_dict


