def get_init_params(hidden_size=32, num_experts=4, batch_size=2, seq_length=4, shared_expert_num=0):
    """Generate initialization parameters."""
    np.random.seed(42)
    # inputs: (batch_size, seq_length, hidden_size)
    inputs = 0.01 * np.random.randn(batch_size, seq_length, hidden_size).astype(np.float32)

    # router.weight: (num_experts, hidden_size)
    router_weight = 0.01 * np.random.randn(num_experts, hidden_size).astype(np.float32)

    # moe_ffn_hidden_size = hidden_size * 4
    ffn_hidden_size = hidden_size * 4

    # experts.weight1: (num_experts * hidden_size, ffn_hidden_size * 2)
    # Note: GLU activation implies output is 2 * ffn_hidden_size
    experts_weight1 = 0.01 * np.random.randn(num_experts * hidden_size, ffn_hidden_size * 2).astype(np.float32)

    # experts.weight2: (num_experts * ffn_hidden_size, hidden_size)
    experts_weight2 = 0.01 * np.random.randn(num_experts * ffn_hidden_size, hidden_size).astype(np.float32)

    params = {
        "inputs": inputs,
        "router_weight": router_weight,
        "experts_weight1": experts_weight1,
        "experts_weight2": experts_weight2
    }

    if shared_expert_num > 0:
        # shared_experts.linear_fc1.weight: (shared_expert_num * 2 * ffn_hidden_size, hidden_size)
        shared_fc1_out = shared_expert_num * 2 * ffn_hidden_size
        shared_fc1 = 0.01 * np.random.randn(shared_fc1_out, hidden_size).astype(np.float32)

        # shared_experts.linear_fc2.weight: (hidden_size, shared_expert_num * ffn_hidden_size)
        shared_fc2_in = shared_expert_num * ffn_hidden_size
        shared_fc2 = 0.01 * np.random.randn(hidden_size, shared_fc2_in).astype(np.float32)

        params["shared_fc1"] = shared_fc1
        params["shared_fc2"] = shared_fc2

    return params


